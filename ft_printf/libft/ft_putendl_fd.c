#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;

	if (!s || !fd)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
	write(fd, "\n", 1);
	return ;
}
