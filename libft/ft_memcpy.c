#include "libft.h"

void	*ft_memcpy(void *dst, void const *src, size_t n)
{
	unsigned int		i;
	unsigned char		*d;
	unsigned const char	*s;

	d = dst;
	s = src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		i++;
	}
	return (dst);
}
