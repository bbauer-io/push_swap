#include "libft.h"

char	*ft_strrchr(char const *s, int c)
{
	char		ch;
	char const	*sc = NULL;

	ch = c;
	while (*s != '\0')
	{
		if (*s == ch)
			sc = s;
		s++;
	}
	if (*s == '\0' && c == '\0')
		sc = s;
	return ((char *)sc);
}
