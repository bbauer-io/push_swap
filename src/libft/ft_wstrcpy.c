#include "libft.h"

wchar_t		*ft_wstrcpy(wchar_t *dst, wchar_t const *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
